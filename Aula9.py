# Nessa aula, vamos aprender operações com String no Python. As principais operações que vamos aprender são
# o Fatiamento de String, Análise com len(), count(), find(), transformações com replace(), upper(), lower(), capitalize(),
# title(), strip(), junção com join().

frase = 'Curso em vídeo Python'

print(frase[2:12])

print(frase[:12])

print(frase[12:])

print(frase[::2])

print(frase.replace('Python', 'Android'))

dividido = frase.split()
print(dividido[2][3])

